# Take 3 strings with all the string declaration techniques.

# 1st string declaration type using single quote

a = 'Hello this is a string'
print(a)
print(type(a)) # to find the class of an variable

# 2nd string declaration type using double quotes

b = "Hello this a string using double quotes"
print(b)
print(type(b)) # to find the class of an variable

# 3rd string declaration type using triple quotes

d = '''Hello this a string using triple quotes'''
print(d)
print(type(d)) # to find the class of an variable